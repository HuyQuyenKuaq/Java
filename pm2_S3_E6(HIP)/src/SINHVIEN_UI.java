    SINHVIEN dssv[]=new SINHVIEN[100];// khai báo mảng lưu danh sách sinh viên 
    int k=0; // biến biểu thị số sinh viên 
    DefaultListModel model =new DefaultListModel();
    boolean gt=true;
    
    public void addListMasv(){// thêm danh sách sinh viên vào list 
        InsertArray(k);
        model.addElement(dssv[k].getMa());
        list_msv.setModel(model); k++;       
    }      
    
     public void InsertArray(int k){// chèn dữ liệu vào mảng để in ra list_masv
         String ma=txt_ma.getText();
         String ht=txt_ht.getText();
         String dc=txt_dc.getText();
         boolean gt=true;
         if(rb_nam.isSelected()) gt=true;
         if(rb_nu.isSelected()) gt=false;
         //boolean gt=rb_nam.isEnabled();
         Date ns=new Date();
         SINHVIEN sv=new SINHVIEN(ma,ht,gt,ns,dc);
         dssv[k]=sv;         
     }// thêm danh sách sinh viên vào mảng 
     public void setNull(){
         txt_ma.setText("");
         txt_ht.setText("");
         txt_dc.setText("");
         txt_ns.setText("");        
}     // làm danh rỗng các textfiel 
