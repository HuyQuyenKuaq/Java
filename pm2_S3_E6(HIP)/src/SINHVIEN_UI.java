    private void list_msvMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_list_msvMouseClicked
        // TODO add your handling code here:
        for(int i=0;i<k;i++){
            if(list_msv.getSelectedValue().equals(dssv[i].getMa())){
                txt_ma.setText(dssv[i].getMa());
                txt_ht.setText(dssv[i].getHt());
                txt_ns.setText(dssv[i].getNs()+" ");
                txt_dc.setText(dssv[i].getDc());
                
            }
        }        
    }//GEN-LAST:event_list_msvMouseClicked

